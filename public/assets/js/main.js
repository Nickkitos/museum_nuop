function toggleMenu() {
    var menu = document.querySelector('.menu_items');
    menu.style.display = (menu.style.display === 'block') ? 'none' : 'block';
}

function closeMenu() {
    var menu = document.querySelector('.menu_items');
    menu.style.display = (menu.style.display === 'none') ? 'block' : 'none';
}





